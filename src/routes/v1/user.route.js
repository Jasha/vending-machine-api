const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const userValidation = require('../../validations/user.validation');
const userController = require('../../controllers/user.controller');

const router = express.Router();

router.post('', validate(userValidation.createUser), userController.createUser);

router.route('/').get(auth(), validate(userValidation.getUsers), userController.getUsers);

router
  .route('/:userId')
  .get(auth(), validate(userValidation.getUser), userController.getUser)
  .patch(auth(), validate(userValidation.updateUser), userController.updateUser)
  .delete(auth(), validate(userValidation.deleteUser), userController.deleteUser);

router
  .route('/deposit')
  .post(auth('manageDeposit'), validate(userValidation.increaseDeposit), userController.increaseDeposit);

router.route('/reset').post(auth('manageDeposit'), validate({}), userController.resetDeposit);

module.exports = router;

